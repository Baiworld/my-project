#!/bin/bash
# ============================================================================
# stop_pipeline.sh — 停止整个数据管道
# ============================================================================

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

echo "=============================================="
echo "  冷启动推荐系统 — 停止数据管道"
echo "=============================================="

echo ""
echo "[1/2] 停止逐行投喂脚本..."
bash "$SCRIPT_DIR/feed_data.sh" stop

echo ""
echo "[2/2] 停止 Flume Agent..."
pkill -f "flume-pipeline.conf" 2>/dev/null && echo "  ✓ Flume 已停止" || echo "  Flume 未在运行"

echo ""
echo "=============================================="
echo "  数据管道已停止"
echo "=============================================="
